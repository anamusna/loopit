import { allItems } from "./items";
const getUserItems = (userId: string, users: UserProfile[]): Item[] => {
  return allItems
    .filter((item) => item.ownerId === userId)
    .map((item) => ({
      ...item,
      ownerName:
        users.find((user) => user.id === userId)?.name || item.ownerName,
      ownerAvatar:
        users.find((user) => user.id === userId)?.avatar || item.ownerAvatar,
    }));
};
const initializeUserItems = (users: UserProfile[]): UserProfile[] => {
  return users.map((user) => ({
    ...user,
    items: getUserItems(user.id, users),
    environmentalStats: {
      ...user.environmentalStats,
      totalCarbonSaved: getUserItems(user.id, users).reduce(
        (total, item) => total + (item.environmentalImpact?.carbonSaved || 0),
        0
      ),
      totalWaterSaved: getUserItems(user.id, users).reduce(
        (total, item) => total + (item.environmentalImpact?.waterSaved || 0),
        0
      ),
      totalLandfillSaved: getUserItems(user.id, users).reduce(
        (total, item) => total + (item.environmentalImpact?.landfillSaved || 0),
        0
      ),
    },
  }));
};
